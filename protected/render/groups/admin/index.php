        <script>
            $(document).ready(function() {
                var groups_table = $("#groups-table").bootgrid({
                    requestHandler: function (request) {
                        var model = {
                            search: request.searchPhrase,
                            current: request.current,
                            rows: request.rowCount
                        };

                        for (var key in request.sort) {
                            model.sort_by = key;
                            model.sort_direction = request.sort[key];
                        }

                        return model;
                    },
                    ajax: true,
                    ajaxSettings: {
                        method: 'POST',
                        cache: false
                    },
                    url: '<?= APP::Module('Routing')->root ?>admin/groups/api/list.json',
                    css: {
                        icon: 'zmdi icon',
                        iconColumns: 'zmdi-view-module',
                        iconDown: 'zmdi-chevron-down pull-left',
                        iconRefresh: 'zmdi-refresh',
                        iconUp: 'zmdi-chevron-up pull-left'
                    },
                    formatters: {
                        actions: function(column, row) {
                            return '<a href="<?= APP::Module('Routing')->root ?>admin/groups/edit/' + row.id_token + '" class="btn btn-sm btn-default btn-icon waves-effect waves-circle"><span class="zmdi zmdi-edit"></span></a> ' + 
                                   '<a href="javascript:void(0)" class="btn btn-sm btn-default btn-icon waves-effect waves-circle remove-group" data-group-id="' + row.id + '"><span class="zmdi zmdi-delete"></span></a>';
                        },
                        name: function(column, row) {
                            return '<a href="<?= APP::Module('Routing')->root ?>admin/groups/users/' + row.id_token + '">' + row.name + '</a> ';
                        }
                    }
                }).on('loaded.rs.jquery.bootgrid', function () {
                    groups_table.find('.remove-group').on('click', function (e) {
                        var group_id = $(this).data('group-id');

                        swal({
                            title: 'Вы действительно хотите удалить группу?',
                            text: 'Восстановление будет невозможно',
                            type: 'warning',
                            showCancelButton: true,
                            confirmButtonColor: '#DD6B55',
                            confirmButtonText: 'Yes',
                            cancelButtonText: 'No',
                            closeOnConfirm: false,
                            closeOnCancel: true
                        }, function(isConfirm){
                            if (isConfirm) {
                                $.post('<?= APP::Module('Routing')->root ?>admin/groups/api/remove.json', {
                                    id: group_id
                                }, function() { 
                                    groups_table.bootgrid('reload', true);
                                    swal('Готово!', 'Группа успешно удалена', 'success');
                                });
                            }
                        });
                    });
                });
            });
        </script>
